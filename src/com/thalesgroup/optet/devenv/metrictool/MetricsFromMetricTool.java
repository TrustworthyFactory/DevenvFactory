package com.thalesgroup.optet.devenv.metrictool;

public class MetricsFromMetricTool {

	private String ID;
	private String name;
	private String higherdesirable;
	private String context;
	private String description;
	private String metric_computation;
	private String formula;
	public String getID() {
		return ID;
	}
	public void setID(String iD) {
		ID = iD;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getHigherdesirable() {
		return higherdesirable;
	}
	public void setHigherdesirable(String higherdesirable) {
		this.higherdesirable = higherdesirable;
	}
	public String getContext() {
		return context;
	}
	public void setContext(String context) {
		this.context = context;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getMetric_computation() {
		return metric_computation;
	}
	public void setMetric_computation(String metric_computation) {
		this.metric_computation = metric_computation;
	}
	public String getFormula() {
		return formula;
	}
	public void setFormula(String formula) {
		this.formula = formula;
	}
	
	
	
}
